/*

Example 1:

Input: 
Person table:
+----+---------+
| id | email   |
+----+---------+
| 1  | a@b.com |
| 2  | c@d.com |
| 3  | a@b.com |
+----+---------+
Output: 
+---------+
| Email   |
+---------+
| a@b.com |
+---------+
Explanation: a@b.com is repeated two times.
*/


SELECT email ---it returns email only 
FROM Person
GROUP BY Email --- as column name Email
HAVING COUNT(email) > 1; --- given condition if more than 1
